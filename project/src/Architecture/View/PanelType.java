package Architecture.View;

/**
 * 面板枚举
 * 
 * 注意:
 * 当新建一个面板时,在这个枚举类型中要添加同名的枚举
 * 
 * 例如:
 * 创建了一个名为MainMenuPanel的面板,那么需要再这个枚举中添加MainMenuPanel枚举
 */
public enum PanelType {
    MainPanel,
    ClientPanel,
    SalerPanel,
    ClientLogPanel,
    SalerLogPanel,
    SystemManagerPanel,
    SearchSoftWarePanel
}
